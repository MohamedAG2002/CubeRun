#pragma once

#include "core/defines.h"

#include <glm/vec3.hpp>

// Mesh consts
////////////////////////////////////////////////////////
#define CUBE_VERTICES 24
#define CUBE_INDICES  36
////////////////////////////////////////////////////////

// Mesh struct
////////////////////////////////////////////////////////
struct mesh_t 
{
  u32 vao_id, vbo_id, ebo_id;

  glm::vec3 vertices[CUBE_VERTICES];
  usizei vertex_count;
  
  u32 indices[CUBE_INDICES];
  usizei index_count;
};
////////////////////////////////////////////////////////

// Mesh functions
////////////////////////////////////////////////////////
mesh_t* mesh_load();
void mesh_destroy(mesh_t* mesh);
